/**
 * build-ecosystem.mjs
 *
 * Reads docs/ecosystem/stack.yml and rewrites the ECOSYSTEM-DATA block inside
 * docs/_components/EcosystemDiagram.tsx.  Everything outside that block is
 * left untouched — component logic, styles, and imports are hand-maintained.
 *
 * Usage:  node scripts/build-ecosystem.mjs
 *         npm run docs:ecosystem
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import yaml from "js-yaml";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const STACK_YML = path.join(ROOT, "docs", "ecosystem", "stack.yml");
const COMPONENT = path.join(ROOT, "docs", "_components", "EcosystemDiagram.tsx");
const START_MARKER = "// ECOSYSTEM-DATA:START — generated by scripts/build-ecosystem.mjs";
const END_MARKER = "// ECOSYSTEM-DATA:END";

// ---------------------------------------------------------------------------
// Load + validate stack.yml
// ---------------------------------------------------------------------------

const stack = yaml.load(fs.readFileSync(STACK_YML, "utf8"));

const totalUnits = stack.columns.reduce((s, c) => s + c.width, 0);
let warnings = 0;

for (const row of stack.rows) {
  const rowTotal = row.cells.reduce((s, c) => s + c.width, 0);
  if (rowTotal > totalUnits) {
    console.warn(`[ecosystem] WARNING: row "${row.id}" cell widths sum to ${rowTotal} > ${totalUnits} units — clamping`);
    warnings++;
  }
}

const projectById = new Map(stack.projects.map((p) => [p.id, p]));

// Verify all project references in cells exist in registry
for (const row of stack.rows) {
  for (const cell of row.cells) {
    for (const pid of cell.projects) {
      if (!projectById.has(pid)) {
        console.warn(`[ecosystem] WARNING: project "${pid}" in row "${row.id}" not found in registry`);
        warnings++;
      }
    }
  }
}

// ---------------------------------------------------------------------------
// Generate TypeScript data block
// ---------------------------------------------------------------------------

function q(str) {
  return str == null ? "undefined" : JSON.stringify(str);
}

function renderOverview(proj) {
  // overview comes from a separate projects/*.md file in stack.yml via `overview` field.
  // We embed the description text directly; the build script reads the .md file.
  const overviewPath = proj.overview
    ? path.join(ROOT, "docs", "ecosystem", proj.overview)
    : null;

  if (!overviewPath || !fs.existsSync(overviewPath)) {
    return proj.overview ? `undefined /* missing: ${proj.overview} */` : "undefined";
  }

  const raw = fs.readFileSync(overviewPath, "utf8").trim();

  // Extract title (first # heading) and description (remaining paragraphs, no links/headings).
  const lines = raw.split("\n");
  let title = proj.name;
  const bodyLines = [];
  let inLearnMore = false;
  let learnMoreHref = null;

  for (const line of lines) {
    const headingMatch = line.match(/^#+\s+(.+)/);
    if (headingMatch) {
      if (title === proj.name) title = headingMatch[1].trim();
      continue;
    }
    // Capture "Learn more" link
    const linkMatch = line.match(/\[Learn more[^\]]*\]\(([^)]+)\)/i);
    if (linkMatch) {
      learnMoreHref = linkMatch[1];
      continue;
    }
    bodyLines.push(line);
  }

  const description = bodyLines
    .join("\n")
    .replace(/\*\*([^*]+)\*\*/g, "$1")   // strip bold markers
    .replace(/`([^`]+)`/g, "$1")          // strip inline code markers
    .replace(/\[([^\]]+)\]\([^)]+\)/g, "$1") // strip links, keep text
    .trim()
    .replace(/\n{2,}/g, " ")              // collapse paragraphs to single text
    .replace(/\n/g, " ")
    .replace(/\s{2,}/g, " ");

  // Prefer the project href for "Learn more" if the md file doesn't have one
  const href = learnMoreHref || proj.href || null;

  return `{
        title: ${q(title)},
        description: ${q(description)},${href ? `\n        href: ${q(href)},` : ""}
      }`;
}

function renderProject(proj) {
  const lines = [
    `    {`,
    `      id: ${q(proj.id)}, name: ${q(proj.name)}, category: ${q(proj.category)},`,
  ];
  if (proj.href) lines.push(`      href: ${q(proj.href)},`);
  if (proj.nvidia_participates) lines.push(`      nvidia_participates: true,`);
  if (!proj.overview) lines.push(`      isStatic: true,`);
  const overview = renderOverview(proj);
  if (overview !== "undefined") lines.push(`      overview: ${overview},`);
  lines.push(`    },`);
  return lines.join("\n");
}

function renderCell(cell) {
  const projs = cell.projects.map((id) => q(id)).join(", ");
  const label = cell.label ? `, label: ${q(cell.label)}` : "";
  return `        { width: ${cell.width}${label}, projects: [${projs}] },`;
}

function renderRow(row) {
  return [
    `    {`,
    `      id: ${q(row.id)}, label: ${q(row.label)},`,
    `      cells: [`,
    ...row.cells.map(renderCell),
    `      ],`,
    `    },`,
  ].join("\n");
}

function renderColumn(col) {
  return `    { id: ${q(col.id)}, label: ${q(col.label)}, width: ${col.width} },`;
}

const dataBlock = [
  `const DATA: EcosystemData = {`,
  `  columns: [`,
  ...stack.columns.map(renderColumn),
  `  ],`,
  `  rows: [`,
  ...stack.rows.map(renderRow),
  `  ],`,
  `  projects: [`,
  ...stack.projects.map(renderProject),
  `  ],`,
  `};`,
].join("\n");

// ---------------------------------------------------------------------------
// Splice into component file
// ---------------------------------------------------------------------------

const src = fs.readFileSync(COMPONENT, "utf8");
const startIdx = src.indexOf(START_MARKER);
const endIdx = src.indexOf(END_MARKER);

if (startIdx === -1 || endIdx === -1) {
  console.warn(`[ecosystem] SKIP: ECOSYSTEM-DATA markers not found in ${COMPONENT} — skipping data injection`);
  process.exit(0);
}

// Keep everything up to and including the start marker line, then the new data,
// then the end marker onward.
const beforeStart = src.slice(0, startIdx + START_MARKER.length);
const afterEnd = src.slice(endIdx);

const updated = `${beforeStart}\n${dataBlock}\n${afterEnd}`;
fs.writeFileSync(COMPONENT, updated, "utf8");

// Summary
const projectCount = stack.projects.length;
const cellCount = stack.rows.reduce((s, r) => s + r.cells.length, 0);
console.log(`[ecosystem] ${projectCount} projects, ${cellCount} cells across ${stack.rows.length} rows`);
if (warnings) console.log(`[ecosystem] ${warnings} warning(s)`);
